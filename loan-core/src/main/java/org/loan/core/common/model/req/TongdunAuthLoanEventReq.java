package org.loan.core.common.model.req;

/**
 * @author liuzx
 * @desc 同盾信用分借款场景
 * @create 2018-06-15 14:31
 */
public class TongdunAuthLoanEventReq extends TongdunAuthBaseEventReq {

    /**
     * 申请借款金额
     */
    private String loan_amount;
    /**
     * 申请借款期限
     */
    private String loan_term;
    /**
     * 期限单位，默认是月，填写时可以为天或月:DAY,MONTH
     */
    private String loan_term_unit;
    /**
     * 申请借款日期，格式yyyy-MM-dd 默认当前日期
     */
    private String loan_date;
    /**
     * 姓名
     */
    private String name;
    /**
     * 身份证号
     */
    private String id_number;
    /**
     * 手机号
     */
    private String mobile;
    /**
     * 银行卡
     */
    private String card_number;
    /**
     * IP地址
     */
    private String ip_address;

    public String getLoan_amount() {
        return loan_amount;
    }

    public void setLoan_amount(String loan_amount) {
        this.loan_amount = loan_amount;
    }

    public String getLoan_term() {
        return loan_term;
    }

    public void setLoan_term(String loan_term) {
        this.loan_term = loan_term;
    }

    public String getLoan_term_unit() {
        return loan_term_unit;
    }

    public void setLoan_term_unit(String loan_term_unit) {
        this.loan_term_unit = loan_term_unit;
    }

    public String getLoan_date() {
        return loan_date;
    }

    public void setLoan_date(String loan_date) {
        this.loan_date = loan_date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId_number() {
        return id_number;
    }

    public void setId_number(String id_number) {
        this.id_number = id_number;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getCard_number() {
        return card_number;
    }

    public void setCard_number(String card_number) {
        this.card_number = card_number;
    }

    public String getIp_address() {
        return ip_address;
    }

    public void setIp_address(String ip_address) {
        this.ip_address = ip_address;
    }

}
